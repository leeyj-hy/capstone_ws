/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint8_t buff[255];
  uint8_t uid[MIFARE_UID_MAX_LENGTH];
  uint8_t key_a[] = {0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF};
  uint32_t pn532_error = PN532_ERROR_NONE;
  int32_t uid_len = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
  printf("Hello!\r\n");
  PN532 pn532;
  PN532_Init(&pn532);
  PN532_GetFirmwareVersion(&pn532, buff);
  if (PN532_GetFirmwareVersion(&pn532, buff) == PN532_STATUS_OK) {
    printf("Found PN532 with firmware version: %d.%d\r\n", buff[1], buff[2]);
  }
  PN532_SamConfiguration(&pn532);
  printf("Waiting for RFID/NFC card...\r\n");
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    // Check if a card is available to read
    uid_len = PN532_ReadPassiveTarget(&pn532, uid, PN532_MIFARE_ISO14443A, 1000);
    if (uid_len == PN532_STATUS_ERROR) {
      printf(".");
    } else {
      printf("Found card with UID: ");
      for (uint8_t i = 0; i < uid_len; i++) {
        printf("%02x ", uid[i]);
      }
      printf("\r\n");
      break;
    }
  }
  printf("To write block 6...\r\n");
  pn532_error = PN532_MifareClassicAuthenticateBlock(&pn532, uid, uid_len,
      6, MIFARE_CMD_AUTH_A, key_a);
  if (pn532_error) {
    printf("Error: 0x%02x\r\n", pn532_error);
    return -1;
  }
  // Write 0x00, 0x11, 0x22, ... to block 6
  for (uint8_t i = 0; i < 16; i++) {
    buff[i] = (i << 4) + i;
  }
  pn532_error = PN532_MifareClassicWriteBlock(&pn532, buff, 6);
  if (pn532_error) {
    printf("Error: 0x%02x\r\n", pn532_error);
    return -1;
  }
  // Read the data from block 6
  for (uint8_t i = 0; i < 16; i++) {
    buff[i] = 0x00;
  }
  pn532_error = PN532_MifareClassicReadBlock(&pn532, buff, 6);
  printf("Data read back: ");
  for (uint8_t i = 0; i < 16; i++) {
    printf("%02x ", buff[i]);
  }
  printf("\r\n");

  /* USER CODE END 3 */
}

